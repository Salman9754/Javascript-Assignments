var selectedBrand = document.getElementById('brandSelect');

for (var brands in watches) {
  selectedBrand.innerHTML += `<option value="${brands}">${brands}</option>`
}

selectedBrand.value = Object.keys(watches)[0];

selectBrand();

function selectBrand(event) {
  var brand;
  

  if (event) {
    brand = event.target.value;  
  } else {
    brand = document.getElementById('brandSelect').value;  
  }

  
  setTimeout(function() {
    var brandData = watches[brand];
    var cardContainer = document.getElementById('cardContainer');
    cardContainer.innerHTML = ''; 

    for (var models in brandData) {
      var watch = brandData[models];

      var card = `<div class="col-lg-4 col-md-6 col-sm-12">
                    <div class="card watch_card m-auto" style="width: 18rem;">
                      <img src="${watch.img}" class="card-img-top" alt="..." width="200px" height="200px">
                      <div class="card-body">
                        <h5 class="card-title fw-bold" style="color: #162433 !important;">${watch.name}</h5>
                        <p id="card-text" class="card-text">${watch.caption}</p>
                        <div class="down d-flex">
                          <span class="price" style="color: #162433 !important;">${watch.price}</span>
                          <a href="#" class="btn card-btn" style="background-color: #162433; color: white;">Buy Now</a>
                        </div>
                      </div>
                    </div>
                  </div>`;
      cardContainer.innerHTML += card;
    }
  }, 1000);  
}
function showLoader() {
  Swal.fire({
    title: 'Loading...',
    html: 'Please wait while we fetch the data.',
    allowOutsideClick: false, 
    showConfirmButton: false,
    didOpen: () => {
      Swal.showLoading();
    },
  });

 
  setTimeout(() => {
    Swal.close(); 
    selectBrand();
  }, 500); 
}


